#include<iostream>
using namespace std;

int main()
{
  int val1,val2,add,sub,mul,div;
  cout<<"Enter Values"<<endl;
  cin>>val1>>val2;
  
  add=val1+val2;
  sub=val1-val2;
  mul=val1*val2;
  div=val1/val2;

  cout<<"Addition:"<<add<<endl;
  cout<<"Substraction:"<<sub<<endl;
  cout<<"Multiplication:"<<mul<<endl;
  cout<<"Division:"<<div<<endl;
  
  return 0;
}



